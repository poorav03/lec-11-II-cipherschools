// Lecture 11 II - Address of Elements In C++ Pt 2
#include <iostream>
using namespace std;

int main(){
    long long int a;
    int b;
    char c[1];
    float d;
    double e;
    cout << "The addresss of a : "<< &a << endl;
    cout << "The addresss of b : "<< &b << endl;
    cout << "The addresss of c : "<< &c << endl;
    cout << "The addresss of d : "<< &d << endl;
    cout << "The addresss of e : "<< &e << endl;

    return 0;
}